    public void setUangkembali(String uangkembali) {
        this.uangkembali = uangkembali;
    }

    public String getTotalbayar() {
        return totalbayar;
    }

    public void setTotalbayar(String totalbayar) {
        this.totalbayar = totalbayar;
    }
    
